import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-md text-sm font-medium ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90",
        destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/90",
        outline: "border border-input bg-background hover:bg-accent hover:text-accent-foreground",
        secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "hover:bg-accent hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
        
        /* Pomodoro Timer Variants */
        "pomodoro-active": "bg-pomodoro text-pomodoro-foreground hover:bg-pomodoro-hover shadow-lg shadow-pomodoro/25 animate-button-press",
        "mode": "bg-background/80 text-foreground hover:bg-secondary-hover border border-button-border rounded-full px-6 py-2 backdrop-blur-sm",
        "control-primary": "bg-pomodoro text-pomodoro-foreground hover:bg-pomodoro-hover shadow-lg shadow-pomodoro/30 rounded-full animate-button-press",
        "control-secondary": "bg-secondary text-secondary-foreground hover:bg-secondary-hover border border-button-border rounded-full shadow-md",
        "control-minimal": "bg-background/60 text-muted-foreground hover:bg-background/80 hover:text-foreground border border-button-border rounded-full shadow-sm backdrop-blur-sm",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3",
        lg: "h-11 rounded-md px-8",
        icon: "h-10 w-10",
        control: "p-0", // Custom size for round control buttons
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  },
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />;
  },
);
Button.displayName = "Button";

export { Button, buttonVariants };
